    public static void saveMap(Context context, String key, int[][][][] map) {
        SharedPreferences.Editor editor = PreferenceManager.getDefaultSharedPreferences(context).edit();
        editor.putBoolean(KEY_MAP + key, true);
        for (int i = 0; i < 10; i++) {
            for (int i2 = 0; i2 < 10; i2++) {
                for (int i3 = 0; i3 < 10; i3++) {
                    for (int i4 = 0; i4 < 10; i4++) {
                        editor.putInt(KEY_MAP + key + i + i2 + i3 + i4, map[i][i2][i3][i4]);
                    }
                }
            }
        }
        editor.apply();
    }

    public static int[][][][] getMap(Context context, String key) {
        SharedPreferences prefs = PreferenceManager.getDefaultSharedPreferences(context);
        if (prefs.contains(KEY_MAP + key)) {
            int[][][][] map = new int[10][][][];
            for (int i = 0; i < 10; i++) {
                map[i] = new int[10][][];
                for (int i2 = 0; i2 < 10; i2++) {
                    map[i][i2] = new int[10][];
                    for (int i3 = 0; i3 < 10; i3++) {
                        map[i][i2][i3] = new int[10];
                        for (int i4 = 0; i4 < 10; i4++) {
                            map[i][i2][i3][i4] = prefs.getInt(KEY_MAP + key + i + i2 + i3 + i4, 0);
                        }
                    }
                }
            }
            return map;
        } else return MAP;
    }

    public static List<RowData> getMapList(Context context, String key) {
        int[][][][] map = getMap(context, key);

        List<RowData> rows = new ArrayList<>();
        for (int i = 0; i < 10; i++) {
            List<SceneryData> row = new ArrayList<>();
            for (int i2 = 0; i2 < 10; i2++) {
                List<List<TileData>> tileRows = new ArrayList<>();
                for (int i3 = 0; i3 < 10; i3++) {
                    List<TileData> tileRow = new ArrayList<>();
                    for (int i4 = 0; i4 < 10; i4++) {
                        switch(map[i][i2][i3][i4]) {
                            case TILE_GRASS:
                                tileRow.add(new GrassTileData(context, (i2 * 10) + i4, (i * 10) + i3));
                                break;
                            case TILE_BUSH:
                                tileRow.add(new BushTileData(context, (i2 * 10) + i4, (i * 10) + i3));
                                break;
                            case TILE_TREE:
                                tileRow.add(new TreeTileData(context, (i2 * 10) + i4, (i * 10) + i3));
                                break;
                            case TILE_HOUSE:
                                tileRow.add(new HouseTileData(context, false, (i2 * 10) + i4, (i * 10) + i3));
                                 break;
                            case TILE_HOUSE_OPEN:
                                tileRow.add(new HouseTileData(context, true, (i2 * 10) + i4, (i * 10) + i3));
                                break;
                            case TILE_SIGN:
                                tileRow.add(new SignTileData(context, (i2 * 10) + i4, (i * 10) + i3));
                                break;
                        }
                    }
                    tileRows.add(tileRow);
                }
                row.add(new SceneryData(i2, i, tileRows));
            }
            rows.add(new RowData(i, row));
        }

        return rows;
    }

    public static String getMessage(Context context, int sceneRow, int sceneColumn, int tileRow, int tileColumn) {
        SharedPreferences prefs = PreferenceManager.getDefaultSharedPreferences(context);
        if (prefs.contains(KEY_MESSAGE + ((sceneRow * 10) + tileRow) + "," + ((sceneColumn * 10) + tileColumn)))
            return prefs.getString(KEY_MESSAGE + ((sceneRow * 10) + tileRow) + "," + ((sceneColumn * 10) + tileColumn), "");
        else {
            switch (sceneRow) {
                case 0:
                    switch (sceneColumn) {
                        case 0:
                            switch (tileRow) {
                                case 0:
                                    switch (tileColumn) {
                                        case 2:
                                            return "This is a thing.";
                                        case 3:
                                            return "This is another thing.";
                                        case 4:
                                            return "This is yet another thing.";
                                        case 5:
                                            return "Cabbages.";
                                    }
                                    break;
                            }
                            break;
                    }
                    break;
            }

            return "";
        }
    }

